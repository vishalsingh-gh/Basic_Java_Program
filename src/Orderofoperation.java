public class Orderofoperation
{
    public static void main(String[] args)
    {
        int p=5;
        System.out.println(p);
        System.out.println(p++);
        System.out.println(p);
        System.out.println(++p);
        System.out.println(p);
        System.out.println("Decrement");
        int q=50;
        System.out.println(q--);
        System.out.println(q);
        System.out.println(--q);
        System.out.println(q);

    }
}
