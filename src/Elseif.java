public class Elseif
{
    public static void main(String[] args) {
        boolean best=false;
        boolean bad=true;
        if(best)
        {
            System.out.println("product is good");
        }
        else if(bad)
        {
            System.out.println("product is bad");
        }
        else
        {
            System.out.println("product is very bad");
        }
    }
}
